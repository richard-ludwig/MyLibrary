public class MyVar {
    private int i;
    private String s;
    private double d;
    private char c;
    private boolean b;

    public MyVar(boolean a){
        this.b=a;
    }
    public MyVar(int a){
        this.i=a;
    }
    public MyVar(double a){
        this.d=a;
    }
    public MyVar(char a){
        this.c=a;
    }
    public MyVar(String a){
        this.s=a;
    }
}
